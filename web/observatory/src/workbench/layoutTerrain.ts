export type LayoutControl =
  | { key: string; label: string; input: "number"; step?: number; min?: number; max?: number }
  | { key: string; label: string; input: "checkbox" }
  | { key: string; label: string; input: "text" }
  | { key: string; label: string; input: "select"; options: Array<{ value: string; label: string }> };

export type LayoutFamilyMeta = {
  id: string;
  order: number;
  label: string;
  description: string;
  bestFor: string[];
};

export type LayoutAlgorithmMeta = {
  id: string;
  label: string;
  familyId: string;
  subgroupLabel?: string;
  status: "runnable" | "reference";
  kind: "browser_local_layout" | "reference_layout" | "exported_coordinate";
  summary: string;
  usedFor: string;
  bestFor: string[];
  controls?: LayoutControl[];
};

function numberControl(key: string, label: string, step?: number, min?: number, max?: number): LayoutControl {
  return { key, label, input: "number", step, min, max };
}

function checkboxControl(key: string, label: string): LayoutControl {
  return { key, label, input: "checkbox" };
}

function textControl(key: string, label: string): LayoutControl {
  return { key, label, input: "text" };
}

function selectControl(key: string, label: string, options: Array<{ value: string; label: string }>): LayoutControl {
  return { key, label, input: "select", options };
}

function entry(meta: LayoutAlgorithmMeta): LayoutAlgorithmMeta {
  return meta;
}

