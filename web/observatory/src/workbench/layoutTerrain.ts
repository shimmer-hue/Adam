export function resolveLayoutFamilies(payloadFamilies?: Array<Partial<LayoutFamilyMeta>> | null): LayoutFamilyMeta[] {
  if (!payloadFamilies?.length) return DEFAULT_LAYOUT_FAMILIES;
  const overrides = new Map(payloadFamilies.map((family) => [String(family.id), family]));
  return DEFAULT_LAYOUT_FAMILIES.map((family) => ({
    ...family,
    ...(overrides.get(family.id) ?? {}),
    bestFor: Array.isArray(overrides.get(family.id)?.bestFor) ? (overrides.get(family.id)?.bestFor as string[]) : family.bestFor,
  }));
}

export function resolveLayoutCatalog(payloadCatalog?: Record<string, any> | null): Record<string, LayoutAlgorithmMeta> {
  const merged: Record<string, LayoutAlgorithmMeta> = {};
  for (const [id, base] of Object.entries(DEFAULT_LAYOUT_CATALOG)) {
    const override = payloadCatalog?.[id] ?? {};
    merged[id] = {
      ...base,
      ...override,
      id,
      bestFor: Array.isArray(override.bestFor) ? override.bestFor : base.bestFor,
      controls: Array.isArray(override.controls) ? override.controls : base.controls,
    };
  }
  for (const [id, value] of Object.entries(payloadCatalog ?? {})) {
    if (merged[id]) continue;
    merged[id] = {
      id,
      label: value?.label ?? id.replaceAll("_", " "),
      familyId: value?.familyId ?? "force_directed",
      subgroupLabel: value?.subgroupLabel,
      status: value?.status ?? (value?.kind === "browser_local_layout" ? "runnable" : "reference"),
      kind: value?.kind ?? "reference_layout",
      summary: value?.summary ?? "Layout terrain item supplied by payload metadata.",
      usedFor: value?.usedFor ?? "Use according to the payload-supplied explanation.",
      bestFor: Array.isArray(value?.bestFor) ? value.bestFor : [],
      controls: Array.isArray(value?.controls) ? value.controls : undefined,
    };
  }
  return merged;
}

export function resolveLayoutDefaults(payloadDefaults?: Record<string, any> | null): Record<string, any> {
  const merged: Record<string, any> = { ...DEFAULT_LAYOUT_DEFAULTS };
  for (const [key, value] of Object.entries(payloadDefaults ?? {})) {
    if (value && typeof value === "object" && !Array.isArray(value)) {
      merged[key] = { ...(DEFAULT_LAYOUT_DEFAULTS[key] ?? {}), ...value };
    } else {
      merged[key] = value;
    }
  }
  return merged;
}

export function humanLayoutLabel(layoutId: string, catalog?: Record<string, LayoutAlgorithmMeta> | null): string {
  if (layoutId.startsWith("layout-run::")) return "Saved layout";
  return catalog?.[layoutId]?.label ?? layoutId.replaceAll("_", " ");
}

export function isRunnableLayout(layoutId: string, catalog?: Record<string, LayoutAlgorithmMeta> | null): boolean {
  return Boolean(catalog?.[layoutId]?.status === "runnable");
}
